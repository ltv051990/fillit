int			ft_ch_in_nb(int nb)
{
	int		l;

	if (!(l = 0) && nb == -2147483648 && (l += 2) != -1)
		nb = 147483648;
	if (nb < 0 && l++ != -1)
		nb *= -1;
	if (nb > 9)
		l += ft_ch_in_nb(nb / 10);
	return (++l);
}
