#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *ts1;

	ts1 = s1;
	while (*ts1)
		ts1++;
	while (*s2 && n--)
		*ts1++ = *s2++;
	*ts1 = '\0';
	return (s1);
}
