#include "libft.h"

char		*ft_itoa(int n)
{
	int		l;
	char	*new;

	l = ft_ch_in_nb(n);
	if (!(new = ft_strnew(l)))
		return (NULL);
	if (n == -2147483648 && (new[1] = '2'))
		n = -147483648;
	if (n < 0 && (new[0] = '-'))
		n *= -1;
	while (l-- && new[l] != '-' && new[l] != '2')
	{
		new[l] = (n % 10 + '0');
		n /= 10;
	}
	return (new);
}
